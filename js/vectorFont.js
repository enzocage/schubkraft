export function drawVectorChar(ctx, char, x, y, scaleX, scaleY, color) {
  const segments = VECTOR_FONT[char.toUpperCase()] || VECTOR_FONT['?'];
  ctx.strokeStyle = color;
  ctx.beginPath();
  for (let i = 0; i < segments.length; i += 4) {
    const px1 = x + segments[i] * scaleX;
    const py1 = y + segments[i+1] * scaleY;
    const px2 = x + segments[i+2] * scaleX;
    const py2 = y + segments[i+3] * scaleY;
    ctx.moveTo(px1, py1);
    ctx.lineTo(px2, py2);
  }
  ctx.stroke();
}

export function drawVectorText(ctx, text, x, y, size, color) {
  const spacing = size * 10;
  for (let i = 0; i < text.length; i++) {
    drawVectorChar(ctx, text[i], x + i * spacing, y, size, size, color);
  }
}
